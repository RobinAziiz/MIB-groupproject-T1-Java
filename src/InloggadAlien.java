    // denna byter lösen, samt kollar så lösenordet är gilltigt med hjälp av valideringsklass.
    private void btnBytLosenordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBytLosenordActionPerformed
        setlosenord();
        if (Validering.textFaltHarVarde(pswGammaltLosen) && Validering.textFaltHarVarde(pswNyttLosen) && Validering.textFaltHarVarde(pswBekraftaLosen)) {
            try {
                if (idb.fetchSingle("SELECT Losenord FROM Alien WHERE Alien_ID =" + AlienLoggaIn.getAlien()).equals(gammaltLosen)) {
                    if (nyttLosen.equals(bekraftaLosen)) {
                        if (Validering.isPasswordValid(pswNyttLosen) && Validering.isPasswordValid(pswBekraftaLosen)) {
                            idb.update("UPDATE Alien SET Losenord = '" + nyttLosen + "' WHERE Alien_ID =" + AlienLoggaIn.getAlien() + ";");
                            JOptionPane.showMessageDialog(rootPane, "Lösenord upddaterat.");
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Nya lösenord matchar inte");
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Fel lösenord");
                }
            } catch (InfException ex) {

            }
        }
    }//GEN-LAST:event_btnBytLosenordActionPerformed


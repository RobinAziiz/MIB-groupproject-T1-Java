    // listar alla aliens i ett valt område.
    private void btnListaAliensActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListaAliensActionPerformed
        try {
            String valjPlats = idb.fetchSingle("SELECT Plats_ID FROM Plats WHERE Benamning ='" + cbPlats.getItemAt(cbPlats.getSelectedIndex()) + "';");
            String fraga = "SELECT Namn, Telefon FROM Alien WHERE Plats ='" + valjPlats + "';";

            ArrayList<HashMap<String, String>> info = idb.fetchRows(fraga);
            txtareaInfo.setText("Namn:" + " ".repeat(16) + "Telefon: \n");
            for (int i = 0; i < info.size(); i++) {
                HashMap<String, String> hashmap = info.get(i);

                System.out.println(hashmap.get("Namn") + " Telefon: " + hashmap.get("Telefon") + "\n");
                txtareaInfo.append((hashmap.get("Namn") + " ".repeat(20)).substring(0, 21) + hashmap.get("Telefon") + "\n");
                txtareaInfo.setCaretPosition(0);
            }
        } catch (InfException ex) {
            Logger.getLogger(InloggadAlien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnListaAliensActionPerformed

   
    // fyller comboboxen för plats
    private void fyllcbPlats() {
        String fraga = "SELECT benamning AS Område FROM plats";

        ArrayList<String> allaPlatser;

        try {
            allaPlatser = idb.fetchColumn(fraga);
            for (String namn : allaPlatser) {
                cbPlats.addItem(namn);
            }
        } catch (InfException e) {
            JOptionPane.showMessageDialog(rootPane, "Fel har uppstått.");
        }
    }


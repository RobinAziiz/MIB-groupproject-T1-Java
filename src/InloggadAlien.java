    // tar dig till din mail applikation vid behov av hjälp.
    private void mail() throws URISyntaxException {
        Desktop desktop;
        if (Desktop.isDesktopSupported() && (desktop = Desktop.getDesktop()).isSupported(Desktop.Action.MAIL)) {
            URI mailto = new URI(getMail());
            try {
                desktop.mail(mailto);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Tyvärr stöds ej denna funktion på din dator.");
                Logger.getLogger(InloggadAlien.class.getName()).log(Level.SEVERE, null, ex);
               
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Tyvärr stöds ej denna funktion på din dator.");
           //throw new RuntimeException("Tyvärr stöds ej denna funktion på din dator."); 
        }
    }

    private String getMail() {
        String mail = "mailto:" + omradesChefTelefon + "@mibskandinavien.se?subject=Hjälp!";
        return mail;
    }



     // Hämtar text som är inskriven i comboboxar och textfält och uppdaterar en Alien i databasen.
    private void btnAndraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAndraActionPerformed
        if(Validering.isPasswordValid(txtFaltLosen) && Validering.textFaltHarVarde(txtFaltNamn) && Validering.isHeltal(txtFaltTelefon)){
        try {
            String valtLosen = String.valueOf(txtFaltLosen.getPassword());
            String valtNamn = txtFaltNamn.getText();
            String valdTelefon = txtFaltTelefon.getText();
            String valdAnsvarigAgent = cbAgent.getItemAt(cbAgent.getSelectedIndex());
            String valdPlats = cbPlats.getItemAt(cbPlats.getSelectedIndex());
            String fragaplats = "SELECT Plats_ID FROM plats WHERE Benamning = '" + valdPlats + "';";
            String valdPlatsID = idb.fetchSingle(fragaplats);
            String andra = "UPDATE alien SET Losenord = '" + valtLosen + "', Namn = '" + valtNamn + "', Telefon = '" + valdTelefon + "', Ansvarig_agent = "
                    + valdAnsvarigAgent + ", Plats = " + valdPlatsID + " WHERE Alien_ID = " + cbAlienID.getItemAt(cbAlienID.getSelectedIndex()) + ";";
            idb.update(andra);
            JOptionPane.showMessageDialog(rootPane, txtFaltNamn.getText() + " har uppdaterats i registret!");
        } catch (Exception ettUndantag) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());

        }
        }
    }//GEN-LAST:event_btnAndraActionPerformed

    // // Denna autofyller alla boxar och textfält, vid ett val av Alien-id.
    private void cbAlienIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbAlienIDActionPerformed
        try {
            if (cbAgent.getItemCount() == 0 && cbPlats.getItemCount() == 0 && cbRas.getItemCount() == 0) {
                fylliCBagentID();
                fylliCbPlats();
                fyllicbRas();
            }
            String alien = cbAlienID.getItemAt(cbAlienID.getSelectedIndex());
            String fraga = "SELECT * FROM alien WHERE Alien_ID = " + alien + ";";
            String fraga2 = "SELECT agent_ID FROM agent;";
            String fraga3 = "SELECT Ansvarig_agent FROM alien WHERE Alien_ID = " + alien + ";";
            String fraga4 = "SELECT benamning FROM plats WHERE Plats_ID = (SELECT Plats FROM alien WHERE Alien_ID = " + alien + ");";
            // Frågor för hämtning av aliens i respektive ras.
            String fraga5 = "SELECT Alien_ID FROM boglodite;";
            String fraga6 = "SELECT Alien_ID FROM squid;";
            String fraga7 = "SELECT Alien_ID FROM worm;";
            String alienRas = "Okänd ras";
            ArrayList<String> allaAgenter = idb.fetchColumn(fraga2);
            //Skapa arrayList för varje ras
            ArrayList<String> bogloditeArrayList = idb.fetchColumn(fraga5);
            ArrayList<String> squidArrayList = idb.fetchColumn(fraga6);
            ArrayList<String> wormArrayList = idb.fetchColumn(fraga7);
            //Anger värde på rasvariabel (måste hårdkodas)
            if (bogloditeArrayList.contains(alien)) {
                alienRas = "Boglodite";
            } else if (squidArrayList.contains(alien)) {
                alienRas = "Squid";
            } else if (wormArrayList.contains(alien)) {
                alienRas = "Worm";
            }

            boolean rasFound = false;
            boolean agentFound = false;
            boolean platsFound = false;
            int rasPosition = 0;
            int agentPosition = 0;
            int platsPosition = 0;
            for (int i = 0; !agentFound && i < allaAgenter.size(); i++) {
                String agent = allaAgenter.get(i);
                if (agent.equals(idb.fetchSingle(fraga3))) {
                    agentFound = true;
                } else {
                    agentPosition++;
                }
            }
            for (int i = 0; !platsFound && i < cbPlats.getItemCount(); i++) {
                String benamning = cbPlats.getItemAt(i);
                if (benamning.equals(idb.fetchSingle(fraga4))) {
                    platsFound = true;
                } else {
                    platsPosition++;
                }
            }
            for (int i = 0; !rasFound && i < cbRas.getItemCount(); i++) {
                String ras = cbRas.getItemAt(i);
                if (ras.equals(alienRas)) {
                    rasFound = true;
                } else {
                    rasPosition++;
                }
            }
            ArrayList<HashMap<String, String>> alienInfo = idb.fetchRows(fraga);
            for (HashMap<String, String> rad : alienInfo) {
                txtFaltNamn.setText(rad.get("Namn"));
                txtFaltLosen.setText(rad.get("Losenord"));
                cbAgent.setSelectedIndex(agentPosition);
                txtFaltTelefon.setText(rad.get("Telefon"));
                cbPlats.setSelectedIndex(platsPosition);
                cbRas.setSelectedIndex(rasPosition);
                fylliExtra();
                //Agent.add
                //bPlats.setSelectedIndex(position);
            }
            fylliExtra();
        } catch (Exception ettUndantag) { //lägger även till nullpointer exception
            JOptionPane.showMessageDialog(null, "Ett fel uppstod!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());

        }
    }//GEN-LAST:event_cbAlienIDActionPerformed


    private void fylliExtra(){
        String ras = cbRas.getItemAt(cbRas.getSelectedIndex());
        String extra = cbAlienID.getItemAt(cbAlienID.getSelectedIndex());
        String fragaBoogies = "SELECT Antal_Boogies FROM boglodite WHERE Alien_ID = " + extra + ";";
        String fragaArmar = "SELECT Antal_Armar FROM squid WHERE Alien_ID = " + extra + ";";
        try{
            String svarBoogies = idb.fetchSingle(fragaBoogies);
            String svarArmar = idb.fetchSingle(fragaArmar);
        
        if (ras.equals("Boglodite")) {
            lblExtra.setVisible(true);
            lblExtra.setText("Antal Boogies");
            txtFaltExtra.setVisible(true);
            txtFaltExtra.setText(svarBoogies);
        }
        if (ras.equals("Squid")) {
            lblExtra.setVisible(true);
            lblExtra.setText("Antal Armar");
            txtFaltExtra.setVisible(true);
            txtFaltExtra.setText("");
            txtFaltExtra.setText(svarArmar);
        }
        if (ras.equals("Worm")) {
            lblExtra.setVisible(false);
            txtFaltExtra.setVisible(false);
        }
        if (ras.equals("Okänd ras")) {
            lblExtra.setVisible(false);
            txtFaltExtra.setVisible(false);
        }
        }
    catch (Exception ettUndantag) { //lägger även till nullpointer exception
            JOptionPane.showMessageDialog(null, "Ett fel uppstod!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());

        }
    }    
    
    

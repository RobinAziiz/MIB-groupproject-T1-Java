    // när knappen trycks så uppdateras databasen med vad som finns i fälten
    private void btnRegistreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistreraActionPerformed
        try {
            String ras = cbRas.getItemAt(cbRas.getSelectedIndex());
            String rasFraga = "";
            //Validera!!
            if (ras.equals("Boglodite") || ras.equals("Squid")) {
                rasFraga = "INSERT INTO " + ras + " VALUES (" + lblAlien.getText() + ", " + txtFaltExtra.getText() + ");";
            } else if (ras.equals("Worm")) {
                rasFraga = "INSERT INTO " + ras + " VALUES ('" + lblAlien.getText() + "');";
            }

            String valtLosen = String.valueOf(txtFaltLosen.getPassword());
            String valtNamn = txtFaltNamn.getText();
            String valtTelefon = txtFaltTelefon.getText();
            String valdAgent = idb.fetchSingle("SELECT Agent_ID FROM agent WHERE namn= '" + cbAgent.getItemAt(cbAgent.getSelectedIndex()) + "'");
            String valdPlats = idb.fetchSingle("SELECT plats_ID FROM Plats WHERE benamning= '" + cbPlats.getItemAt(cbPlats.getSelectedIndex()) + "'");
            String fraga = ("INSERT INTO Alien VALUES (" + fylliAlienID() + ", " + "curdate()" + ", '" + valtLosen + "', '" + valtNamn + "', " + valtTelefon + ", " + valdPlats + ", " + valdAgent + ");");
            
            if (Validering.isPasswordValid(txtFaltLosen) && Validering.textFaltHarVarde(txtFaltNamn) && Validering.isHeltal(txtFaltTelefon)) {
                if (txtFaltExtra.isVisible() && Validering.textFaltHarVarde(txtFaltExtra) && Validering.isHeltal(txtFaltExtra)) {
                    idb.insert(fraga);
                    idb.insert(rasFraga);
                    JOptionPane.showMessageDialog(null, valtNamn + " har registrerats");
                    dispose();
                    if (admin) {
                        new RegistreraAlien(idb, true).setVisible(true);
                    } else {
                        new RegistreraAlien(idb).setVisible(true);
                    }
                } else if (!txtFaltExtra.isVisible()) {
                    idb.insert(fraga);
                    idb.insert(rasFraga);
                    JOptionPane.showMessageDialog(null, valtNamn + " har registrerats");
                    dispose();
                    if (admin) {
                        new RegistreraAlien(idb, true).setVisible(true);
                    } else {
                        new RegistreraAlien(idb).setVisible(true);
                    }
                }
            }
        } catch (InfException ettUndantag) {
            JOptionPane.showMessageDialog(null, "Databasfel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());
        } catch (Exception ettUndantag) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());
        }

    }//GEN-LAST:event_btnRegistreraActionPerformed

    // beroende på vilken ras som är vald så kan det komma upp ett textfält för att ange ex. antal armar.
    private void cbRasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbRasActionPerformed
        String ras = cbRas.getItemAt(cbRas.getSelectedIndex());
        if (ras.equals("Boglodite")) {
            lblExtra.setVisible(true);
            lblExtra.setText("Antal Boogies");
            txtFaltExtra.setVisible(true);
        }
        if (ras.equals("Squid")) {
            lblExtra.setVisible(true);
            lblExtra.setText("Antal Armar");
            txtFaltExtra.setVisible(true);
        }
        if (ras.equals("Worm")) {
            lblExtra.setVisible(false);
            txtFaltExtra.setVisible(false);
        }
        if (ras.equals("Okänd ras")) {
            lblExtra.setVisible(false);
            txtFaltExtra.setVisible(false);
        }
    }//GEN-LAST:event_cbRasActionPerformed


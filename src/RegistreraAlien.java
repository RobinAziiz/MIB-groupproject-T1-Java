    // fyller i comboboxen för agent
    public void fyllicbAgent() {
        try {
            String fraga = "SELECT Namn from agent;";
            ArrayList<String> allaNamn;
            allaNamn = idb.fetchColumn(fraga);

            for (String namn : allaNamn) {
                cbAgent.addItem(namn);
            }

        } catch (InfException ettUndantag) {
            JOptionPane.showMessageDialog(null, "Databasfel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());
        } catch (Exception ettUndantag) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());

        }
    }

    

    // den ger oss ett id som är unikt
    public String fylliAlienID() {
        String stringAlienID = "";
        try {
            String fraga = idb.fetchSingle("SELECT max(Alien_ID) FROM alien;");
            int alienID = Integer.parseInt(fraga) + 1;
            stringAlienID = Integer.toString(alienID);
            lblAlien.setText(stringAlienID);
        
        } catch (InfException ettUndantag) {
            JOptionPane.showMessageDialog(null, "Databasfel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());
        } catch (Exception ettUndantag) {
            JOptionPane.showMessageDialog(null, "Något gick fel!");
            System.out.println("Internt felmeddelande" + ettUndantag.getMessage());
        }

        return stringAlienID;
    }

